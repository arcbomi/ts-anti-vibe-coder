func TestSaveGeneratedQuestionsRequiresExactlyTwenty(t *testing.T) {
	svc := NewService(&fakeStore{})
	_, err := svc.SaveGeneratedQuestions(context.Background(), SaveGeneratedQuestionsRequest{AnalysisJobID: "00000000-0000-0000-0000-000000000001"})
	var appErr *AppError
	if !errors.As(err, &appErr) {
		t.Fatalf("expected AppError, got %T", err)
	}
	if appErr.Code != ErrCodeBadRequest {
		t.Fatalf("expected %s, got %s", ErrCodeBadRequest, appErr.Code)
	}
}

func TestExamQuestionsHideAnswersAndStoreOptionMapping(t *testing.T) {
	store := &fakeStore{questions: []Question{{
		ID:             "00000000-0000-0000-0000-000000000010",
		AnalysisJobID:  "00000000-0000-0000-0000-000000000001",
		Question:       "How are routes connected?",
		OptionA:        "Automatically from filenames.",
		OptionB:        "Manually in router setup.",
		OptionC:        "Loaded from database.",
		OptionD:        "Generated by frontend.",
		CorrectOption:  OptionB,
		Explanation:    "The router setup connects paths to handlers.",
		Difficulty:     DifficultyMedium,
		SourceFilePath: "internal/server/router.go",
	}}}
	svc := NewService(store)

	resp, err := svc.GetExamQuestions(context.Background(), "00000000-0000-0000-0000-000000000001", "00000000-0000-0000-0000-000000000020")
	if err != nil {
		t.Fatalf("GetExamQuestions returned error: %v", err)
	}
	if len(resp.Questions) != 1 {
		t.Fatalf("expected one question, got %d", len(resp.Questions))
	}
	if resp.ExamID != "00000000-0000-0000-0000-000000000020" {
		t.Fatalf("exam id = %q", resp.ExamID)
	}
	if resp.QuestionsCount != 1 {
		t.Fatalf("questions count = %d", resp.QuestionsCount)
	}
	if resp.Questions[0].SourceFilePath != "" {
		t.Fatalf("exam response must not include source_file_path, got %q", resp.Questions[0].SourceFilePath)
	}
	if len(store.mappings) != 4 {
		t.Fatalf("expected four option mappings, got %d", len(store.mappings))
	}
	if resp.ID != "00000000-0000-0000-0000-000000000020" || resp.ExamID != "00000000-0000-0000-0000-000000000020" || resp.QuestionsCount != 1 {
		t.Fatalf("expected exam metadata in response, got %+v", resp)
	}

	key, err := svc.GetAnswerKey(context.Background(), "00000000-0000-0000-0000-000000000020")
	if err != nil {
		t.Fatalf("GetAnswerKey returned error: %v", err)
	}
	if len(key.Answers) != 1 || key.Answers[0].CorrectOption == "" {
		t.Fatalf("expected mapped answer key, got %#v", key.Answers)
	}
}

