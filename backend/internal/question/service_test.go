func TestEnglishOnlyValidationRejectsMostlyNonLatinText(t *testing.T) {
	input := validGeneratedQuestion()
	input.Question = "这是一个中文问题这是一个中文问题"
	if err := validateGeneratedQuestion(input); err == nil {
		t.Fatal("expected non-Latin question text to be rejected")
	}
}

